/**
 * Shared card body for the ready and low-confidence states; the two differ
 * only in whether the third bullet carries the verify flag. Add-to-Tasks
 * and Share state is local to each specimen instance.
 */
function SummaryCardBody({hasFlag}: {hasFlag: boolean}) {
  const [addedIds, setAddedIds] = useState<ReadonlySet<string>>(
    () => new Set([INITIALLY_ADDED_ID]),
  );
  const [isShared, setIsShared] = useState(false);
  const [isFlagVisible, setIsFlagVisible] = useState(hasFlag);

  const handleAdd = (id: string) => {
    setAddedIds(prev => {
      const next = new Set(prev);
      next.add(id);
      return next;
    });
  };

  return (
    <Card padding={3}>
      <VStack gap={3}>
        <CardHeader />
        <Divider />
        <KeyPoints
          flaggedId={isFlagVisible ? LOW_CONFIDENCE.pointId : null}
          onDismissFlag={() => setIsFlagVisible(false)}
        />
        <Divider />
        <DecisionRow />
        <Divider />
        <ActionItems addedIds={addedIds} onAdd={handleAdd} />
        <CardFooter isShared={isShared} onShare={() => setIsShared(true)} />
      </VStack>
    </Card>
  );
}

