const SEGMENT_COUNT = 6;

function ProgressPanel() {
  // One demo slider animates the determinate bar, the segment strip, and
  // the percent ring so the relationship between the three is obvious.
  const [percent, setPercent] = useState(45);
  const [checkoutStep, setCheckoutStep] = useState(1);

  const filledSegments = Math.round((percent / 100) * SEGMENT_COUNT);
  const ringFillStyle: CSSProperties = {
    ...styles.ringFill,
    background: `conic-gradient(var(--color-accent) ${
      percent * 3.6
    }deg, var(--color-border-emphasized) 0deg)`,
  };

  const goToStep = (index: number) => {
    // Completed steps are clickable; upcoming steps are not skippable.
    if (index <= checkoutStep) {
      setCheckoutStep(index);
    }
  };

  return (
    <PatternPanel
      id="progress"
      title="Progress"
      archetype="Application UI / Progress Bars"
      description="A determinate bar, segmented steps strip, and percent ring all driven by one demo slider, plus a 4-step checkout stepper with clickable completed steps."
      >
      <VStack gap={4}>
        <VStack gap={2}>
          <VariantLabel
            label="Demo value"
            hint="Drag the slider — the bar, segments, and ring below all track it."
          />
          <Slider
            label="Demo progress value"
            min={0}
            max={100}
            step={5}
            value={percent}
            onChange={(next: number) => setPercent(next)}
            formatValue={value => `${value}%`}
          />
        </VStack>

        <Grid columns={{minWidth: 240, max: 3}} gap={4}>
          <VStack gap={2}>
            <VariantLabel label="Determinate bar" />
            <ProgressBar
              value={percent}
              max={100}
              label={`Import progress: ${percent}%`}
              isLabelHidden
              variant={percent === 100 ? 'success' : 'accent'}
            />
            <Text type="supporting" color="secondary">
              {percent === 100
                ? 'Import complete.'
                : `Importing ledger rows — ${percent}% done.`}
            </Text>
          </VStack>

          <VStack gap={2}>
            <VariantLabel label="Segmented steps" />
            <div
              style={styles.segmentTrack}
              role="img"
              aria-label={`${filledSegments} of ${SEGMENT_COUNT} segments complete`}>
              {Array.from({length: SEGMENT_COUNT}, (_, index) => (
                <div
                  key={index}
                  style={
                    index < filledSegments
                      ? styles.segmentFilled
                      : styles.segment
                  }
                />
              ))}
            </div>
            <Text type="supporting" color="secondary">
              {filledSegments} of {SEGMENT_COUNT} batches migrated.
            </Text>
          </VStack>

          <VStack gap={2}>
            <VariantLabel label="Percent ring" />
            <HStack gap={3} vAlign="center">
              <div
                style={styles.ringOuter}
                role="img"
                aria-label={`Storage used: ${percent}%`}>
                <div style={ringFillStyle} aria-hidden />
                <Text size="sm" weight="semibold">
                  {percent}%
                </Text>
              </div>
              <Text type="supporting" color="secondary">
                Conic-gradient donut; the label sits in the punched-out
                center.
              </Text>
            </HStack>
          </VStack>
        </Grid>

        <Divider />

        <VStack gap={2}>
          <VariantLabel
            label="Checkout stepper"
            hint="Completed steps are clickable to jump back; upcoming steps stay locked."
          />
          <div style={styles.stepperRow}>
            {CHECKOUT_STEPS.map((step, index) => {
              const isDone = index < checkoutStep;
              const isActive = index === checkoutStep;
              return (
                <button
                  key={step}
                  type="button"
                  style={
                    isActive
                      ? styles.stepButtonActive
                      : isDone
                        ? styles.stepButton
                        : styles.stepButtonUpcoming
                  }
                  onClick={() => goToStep(index)}
                  disabled={index > checkoutStep}
                  aria-current={isActive ? 'step' : undefined}
                  aria-label={
                    isDone
                      ? `${step} (completed — click to return)`
                      : isActive
                        ? `${step} (current step)`
                        : `${step} (upcoming)`
                  }>
                  <span
                    style={isDone ? styles.stepIndexDone : styles.stepIndex}
                    aria-hidden>
                    {isDone ? <Icon icon={CheckIcon} size="xsm" /> : index + 1}
                  </span>
                  <Text size="sm" weight={isActive ? 'semibold' : 'normal'}>
                    {step}
                  </Text>
                </button>
              );
            })}
          </div>
          <HStack gap={2} vAlign="center">
            <Button
              label="Back"
              variant="ghost"
              size="sm"
              isDisabled={checkoutStep === 0}
              onClick={() => setCheckoutStep(step => Math.max(0, step - 1))}
            />
            <Button
              label={
                checkoutStep === CHECKOUT_STEPS.length - 1
                  ? 'Place order'
                  : 'Next'
              }
              variant="primary"
              size="sm"
              isDisabled={checkoutStep === CHECKOUT_STEPS.length - 1}
              onClick={() =>
                setCheckoutStep(step =>
                  Math.min(CHECKOUT_STEPS.length - 1, step + 1),
                )
              }
            />
            <Text type="supporting" color="secondary">
              Step {checkoutStep + 1} of {CHECKOUT_STEPS.length} ·{' '}
              {CHECKOUT_STEPS[checkoutStep]}
            </Text>
          </HStack>
        </VStack>
      </VStack>
    </PatternPanel>
  );
}

