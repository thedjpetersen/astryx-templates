type RetryPhase = 'error' | 'retrying' | 'resolved';

function ErrorRetrySpecimen() {
  const [phase, setPhase] = useState<RetryPhase>('error');
  const timerRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  // The retry simulates a 1 s reload, then resolves. Clean the timer up on
  // unmount so switching panels mid-retry never fires a stale setState.
  useEffect(() => {
    return () => {
      if (timerRef.current != null) {
        clearTimeout(timerRef.current);
      }
    };
  }, []);

  const startRetry = () => {
    setPhase('retrying');
    timerRef.current = setTimeout(() => {
      setPhase('resolved');
      timerRef.current = null;
    }, 1000);
  };

  return (
    <VStack gap={2} style={styles.specimenBody}>
      <VariantLabel
        label="Error state with retry"
        hint="Retry simulates a 1 s reload, then resolves into content."
      />
      {phase === 'resolved' ? (
        <VStack gap={2}>
          <FieldStatus
            variant="detached"
            type="success"
            message="Reloaded — payouts are back."
          />
          <InvoiceListLite rows={RECOVERED_ROWS} />
          <Link onClick={() => setPhase('error')} type="supporting">
            Reset demo
          </Link>
        </VStack>
      ) : phase === 'retrying' ? (
        <VStack gap={2} hAlign="center" style={{minHeight: 120}}>
          <Spinner size="sm" aria-label="Retrying payout fetch" />
          <Text type="supporting" color="secondary">
            Retrying payout fetch…
          </Text>
        </VStack>
      ) : (
        <EmptyState
          icon={<Icon icon={ServerCrashIcon} size="lg" />}
          title="Couldn’t load payouts"
          description="The reporting service returned 503. Your data is safe; only this view failed."
          isCompact
          actions={
            <Button
              label="Retry"
              variant="secondary"
              onClick={startRetry}
              icon={<Icon icon={RefreshCwIcon} size="sm" />}
            />
          }
        />
      )}
    </VStack>
  );
}

