function NumberedPagerSpecimen({isCompact}: {isCompact: boolean}) {
  const [page, setPage] = useState(8);
  const rows = INVOICES.slice((page - 1) * PAGE_SIZE, page * PAGE_SIZE);
  const windowPages = buildPageWindow(page, TOTAL_PAGES);

  const pager = (
    <VStack gap={2}>
      <div
        style={styles.pagerRow}
        role="navigation"
        aria-label="Invoice pages (numbered)">
        <IconButton
          label="Previous page"
          icon={<Icon icon={ChevronLeftIcon} size="sm" />}
          variant="ghost"
          isDisabled={page === 1}
          onClick={() => setPage(current => Math.max(1, current - 1))}
        />
        {windowPages.map((pageNumber, index) =>
          pageNumber === -1 ? (
            <span
              key={`gap-${index}`}
              style={styles.ellipsisCell}
              aria-hidden>
              <Text type="supporting" color="secondary">
                …
              </Text>
            </span>
          ) : (
            <Button
              key={pageNumber}
              label={String(pageNumber)}
              variant={pageNumber === page ? 'primary' : 'ghost'}
              size="sm"
              aria-current={pageNumber === page ? 'page' : undefined}
              onClick={() => setPage(pageNumber)}
            />
          ),
        )}
        <IconButton
          label="Next page"
          icon={<Icon icon={ChevronRightIcon} size="sm" />}
          variant="ghost"
          isDisabled={page === TOTAL_PAGES}
          onClick={() =>
            setPage(current => Math.min(TOTAL_PAGES, current + 1))
          }
        />
      </div>
      <Text type="supporting" color="secondary">
        Page {page} of {TOTAL_PAGES} · the list{' '}
        {isCompact ? 'below' : 'beside this pager'} is the live window.
      </Text>
    </VStack>
  );

  const resultList = (
    <VStack gap={1}>
      <Text type="label" color="secondary">
        Invoices {(page - 1) * PAGE_SIZE + 1}–{page * PAGE_SIZE} of{' '}
        {TOTAL_ROWS}
      </Text>
      <InvoiceList rows={rows} />
    </VStack>
  );

  return (
    <VStack gap={2}>
      <VariantLabel
        label="Numbered with ellipsis"
        hint={`Windows ${TOTAL_PAGES} fixture pages; the result list re-slices on every click.`}
      />
      {isCompact ? (
        <VStack gap={3}>
          {pager}
          {resultList}
        </VStack>
      ) : (
        <HStack gap={4} vAlign="start">
          <StackItem size="fill">{pager}</StackItem>
          <StackItem size="fill">{resultList}</StackItem>
        </HStack>
      )}
    </VStack>
  );
}

